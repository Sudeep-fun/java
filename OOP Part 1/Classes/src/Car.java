public class Car {
    private int doors;
    private int wheels;
    private String model;
    private String color;

    public void setModel(String model) {
        this.model = model;
    }

    public int getDoors() {
        return doors;
    }
    public int getWheels() {
        return wheels;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getModel() {
        return this.model;
    }

}
